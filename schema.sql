DROP TABLE IF EXISTS Teilgenommen;
DROP TABLE IF EXISTS KOSpiel;
DROP TABLE IF EXISTS Ligaspieler;
DROP TABLE IF EXISTS Ligaspiel;
DROP TABLE IF EXISTS Turnierspiel;
